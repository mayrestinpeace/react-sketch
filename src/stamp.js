class Stamp extends FabricCanvasTool {

  configureCanvas(props) {
    let canvas = this._canvas;
    canvas.isDrawingMode = canvas.selection = false;
    canvas.forEachObject((o) => o.selectable = o.evented = false);
    this._stampType = props.stampType;
  }

  doMouseDown(o) {
    let canvas = this._canvas;
    var pointer = canvas.getPointer(o.e);
    let svg;
    switch (this._stampType) {
      case StampType.Arrow:
        svg = arrowSVG
        break;
      case StampType.Heart:
        svg = heartSVG
        break;
      case StampType.Check:
        svg = checkSVG
        break;
      case StampType.Cross:
        svg = crossSVG
        break;
      case StampType.Star:
        svg = starSVG
        break;
      case StampType.Question:
        svg = questionSVG
        break;
    }
    fabric.loadSVGFromString(svg, function(objects, options) {
      var obj = fabric.util.groupSVGElements(objects, options);
      obj.scaleToHeight(32).set({left: pointer.x-16,top: pointer.y-16})
      canvas.add(obj).renderAll();
      obj.setCoords();
    });
  }

}

export default Stamp;